import { assert } from "tsafe";

export class PriorityItem {
    item: string;
    constructor(item: string) {
        assert(scoreArray.includes(item));
        this.item = item;
    }
    getScore = () => {
        const index = scoreArray.findIndex((e) => e === this.item);
        return index + 1;
    };
}

